     <h1>About Caro</h1>
        <p>Welcome to the Caro game! This is a simple implementation of the classic game of Caro, also known as Gomoku.</p>
        <p>In this game, two players take turns placing their marks (X or O) on a grid. The objective is to get five of your marks in a row, either horizontally, vertically, or diagonally.</p>
        <p>To play, simply click on the grid to place your mark. The game will alternate turns between the two players until one player wins or the game ends in a draw.</p>
        <p>Enjoy the game!</p>   
